#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	char			*dest;

	i = 0;
	dest = (char *)b;
	while (i < len)
	{
		dest[i] = c;
		i++;
	}
	return (b);
}
