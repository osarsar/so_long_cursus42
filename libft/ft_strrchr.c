#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*b;
	int		i;

	b = (char *)s;
	i = 0;
	while (b[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (b[i] == (char)c)
		{
			return (b + i);
		}
		i--;
	}
	return (NULL);
}
